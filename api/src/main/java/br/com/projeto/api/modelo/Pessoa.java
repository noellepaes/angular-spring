package br.com.projeto.api.modelo;

public class Pessoa {
    
    //Atributo
    private String nome;
    private String idade;


    //Get e Set
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public String getIdade() {
        return idade;
    }
    public void setIdade(String idade) {
        this.idade = idade;
    }
}
